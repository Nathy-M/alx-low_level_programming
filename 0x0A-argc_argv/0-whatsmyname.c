#include "main.h"
#include <stdlib.h>
#include <stdio.h>

/**
 * main - prints program name.
 * @argc: represent number of the command line entered while running the program.
 * @argv: an array of pointers to the strings.
 *
 * Return: Always 0.
 */

int main(int argc, char *argv[])
{
	_putchar(argv[0]);
	_putchar('\n');
	return (0);
}
